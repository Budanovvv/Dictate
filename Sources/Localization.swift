import Foundation
import SwiftUI

/// UI language. Base strings are English; translations come from the tables below.
enum AppLanguage: String, CaseIterable, Identifiable {
    case system, en, ru, uk, es, pt, fr, de, zh, ja, ko, vi, tl
    var id: String { rawValue }

    /// Label for the picker item (in its own language). "Follow system" is
    /// shown in the *system's* language, not the current app language, so it
    /// stays stable and reads naturally regardless of what's selected.
    var label: String {
        switch self {
        case .system: return Localization.shared.string("Follow system", in: Localization.systemLanguage)
        case .en: return "English"
        case .ru: return "Русский"
        case .uk: return "Українська"
        case .es: return "Español"
        case .pt: return "Português"
        case .fr: return "Français"
        case .de: return "Deutsch"
        case .zh: return "中文"
        case .ja: return "日本語"
        case .ko: return "한국어"
        case .vi: return "Tiếng Việt"
        case .tl: return "Filipino"
        }
    }
}

/// Translation store; ObservableObject so views re-render when the language changes.
final class Localization: ObservableObject {
    static let shared = Localization()

    @Published private(set) var language: AppLanguage

    private init() {
        let stored = UserDefaults.standard.string(forKey: "uiLanguage") ?? AppLanguage.system.rawValue
        self.language = AppLanguage(rawValue: stored) ?? .system
    }

    func setLanguage(_ lang: AppLanguage) {
        language = lang
        UserDefaults.standard.set(lang.rawValue, forKey: "uiLanguage")
    }

    /// The language the system resolves to (never .system).
    static var systemLanguage: AppLanguage {
        let code = Locale.preferredLanguages.first?
            .split(separator: "-").first.map(String.init) ?? "en"
        switch code {
        case "ru": return .ru
        case "uk": return .uk
        case "es": return .es
        case "pt": return .pt
        case "fr": return .fr
        case "de": return .de
        case "zh": return .zh
        case "ja": return .ja
        case "ko": return .ko
        case "vi": return .vi
        case "fil", "tl": return .tl   // macOS reports "fil" for Filipino
        default: return .en
        }
    }

    /// Effective language, taking "Follow system" into account.
    var effective: AppLanguage {
        language == .system ? Self.systemLanguage : language
    }

    func translate(_ en: String) -> String { string(en, in: effective) }

    /// Translate into a specific language — used for labels that must render
    /// in a fixed language regardless of the current UI language.
    func string(_ en: String, in lang: AppLanguage) -> String {
        switch lang {
        case .ru: return Self.ru[en] ?? en
        case .uk: return Self.uk[en] ?? en
        case .es: return Self.es[en] ?? en
        case .pt: return Self.pt[en] ?? en
        case .fr: return Self.fr[en] ?? en
        case .de: return Self.de[en] ?? en
        case .zh: return Self.zh[en] ?? en
        case .ja: return Self.ja[en] ?? en
        case .ko: return Self.ko[en] ?? en
        case .vi: return Self.vi[en] ?? en
        case .tl: return Self.tl[en] ?? en
        default: return en
        }
    }
}

/// Translates a base-English string.
func L(_ en: String) -> String { Localization.shared.translate(en) }

/// Translation with substituted arguments: Lf("Step %d of %d", 1, 4).
func Lf(_ en: String, _ args: CVarArg...) -> String {
    String(format: Localization.shared.translate(en), arguments: args)
}

